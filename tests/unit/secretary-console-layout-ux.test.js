  assert.doesNotMatch(shellCss, /margin-left:\s*220px;/);
  assert.match(meetingsCss, /grid-template-columns:\s*minmax\(320px,\s*var\(--meetings-list-width,\s*360px\)\)\s+12px\s+minmax\(0,\s*1fr\);/);
  assert.match(businessHubCss, /grid-template-columns:\s*minmax\(320px,\s*var\(--business-list-width,\s*380px\)\)\s+12px\s+minmax\(0,\s*1fr\);/);
  assert.match(meetingsCss, /\.pane-splitter/);
  assert.match(businessHubCss, /\.pane-splitter/);
