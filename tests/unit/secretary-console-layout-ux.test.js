  assert.match(stripeAdminHtml, /class="tab[^"]*tab-utility-link/);
  assert.doesNotMatch(stripeAdminHtml, /--cam-/);
  assert.match(productsAdminHtml, /--admin-border-strong:/);
  assert.doesNotMatch(productsAdminHtml, /--cam-/);
  assert.match(stripeAdminHtml, /linear-gradient\(180deg,\s*var\(--admin-bg-start\),\s*var\(--admin-bg-end\)\)/);
  assert.match(productsAdminHtml, /linear-gradient\(180deg,\s*var\(--admin-bg-start\),\s*var\(--admin-bg-end\)\)/);
