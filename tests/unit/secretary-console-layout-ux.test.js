  assert.match(actionItemsTabJs, /aria-haspopup="menu"/);
  assert.match(actionItemsTabJs, /aria-expanded="false"/);
  assert.match(motionsTabJs, /aria-haspopup="menu"/);
  assert.match(reviewsTabJs, /aria-expanded="false"/);
  assert.match(quotesTabJs, /aria-haspopup="menu"/);
  assert.match(settingsViewJs, /const settingsContainer = utilityView;/);
  assert.match(appJs, /registerRoute\("\/admin\/stripe"/);
