  assert.match(dashboardViewJs, /dashboard-lane-card dashboard-click-card/);
  assert.match(dashboardViewJs, /dashboard-feature-card dashboard-click-card/);
  assert.match(dashboardViewJs, /dashboard-calendar-item dashboard-click-card/);
  assert.match(dashboardViewJs, /dashboard-section-link/);
  assert.match(dashboardViewJs, /dashboard-city-status/);
  assert.match(dashboardViewJs, /dashboard-city-actions/);
  assert.match(dashboardViewJs, /model\.cityFocus\.primaryCta/);
  assert.match(dashboardViewJs, /model\.navigationLinks\.feature/);
  assert.match(meetingsViewJs, /pane-splitter/);
  assert.match(meetingsViewJs, /attachPaneSplitter/);
  assert.match(businessHubViewJs, /pane-splitter/);
  assert.match(businessHubViewJs, /attachPaneSplitter/);
  assert.match(meetingHeaderJs, /detail-summary-grid/);
