  assert.match(dashboardCss, /\.dashboard-lanes-grid\s*\{[\s\S]*grid-template-columns:\s*repeat\(2,\s*minmax\(0,\s*1fr\)\);/);
  assert.match(dashboardCss, /\.dashboard-section-head--actionable/);
