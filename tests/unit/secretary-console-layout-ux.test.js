  assert.match(dashboardCss, /\.dashboard-stat-card:focus-visible/);
  assert.match(dashboardCss, /\.dashboard-lane-card:focus-visible/);
  assert.match(dashboardCss, /\.dashboard-feature-card:focus-visible/);
  assert.match(dashboardCss, /\.dashboard-calendar-item:focus-visible/);
  assert.match(meetingsCss, /\.detail-summary-grid/);
