  assert.match(meetingsCss, /\.btn-icon\s*\{[\s\S]*transition:[\s\S]*background-color 0\.2s ease/);
  assert.match(meetingsCss, /\.btn-icon:focus-visible/);
  assert.match(businessHubCss, /\.btn-row-menu:focus-visible/);
  assert.match(shellCss, /\.admin-workspace-grid/);
